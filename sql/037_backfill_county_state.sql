-- 1) Patch county where current value is garbage (== town, or null/empty,
--    or one of the few "NY" / "NJ" tokens Outscraper mis-wrote).
--    Skip rows where county is already a real, non-town-shaped name so
--    re-running won't stomp on a manually-corrected value.
update restr_ai_leaderboard r
set county = z.county
from zip_county_lookup z
where r.zipcode = z.zipcode
  and r.zipcode ~ '^[0-9]{5}$'
  and (
    r.county is null
    or btrim(r.county) = ''
    or lower(coalesce(r.county, '')) = lower(coalesce(r.town, ''))
    or r.county in ('NY', 'NJ', 'PA', 'DE', 'GA', 'SC', 'MA', 'CT', 'FL', 'CA', 'TX')
  );

-- 2) Normalize state to USPS 2-letter abbreviation. Some Outscraper
--    rows wrote "NY" instead of "New York" (339 rows), the rest were
--    already full names like "New Jersey". The lookup provides the
--    canonical 2-letter abbr.
update restr_ai_leaderboard r
set state = z.state_abbr
from zip_county_lookup z
where r.zipcode = z.zipcode
  and r.zipcode ~ '^[0-9]{5}$'
  and r.state is distinct from z.state_abbr;

-- 3) Report what got changed (visible in psql output)
select
  (select count(*) from restr_ai_leaderboard where zipcode ~ '^[0-9]{5}$') as rows_with_zip,
  (select count(*) from restr_ai_leaderboard where county is null or btrim(coalesce(county, '')) = '') as still_no_county,
  (select count(*) from restr_ai_leaderboard where state in ('New York', 'New Jersey', 'Pennsylvania', 'Delaware', 'Georgia')) as still_full_state;

