/**
 * Reads reference/rankmysalon-website.jsx and writes sql/014_seed_salon_ai_leaderboard.sql
 * Run: node scripts/generate-leaderboard-seed-sql.js
 */
const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const { assignAssessmentLevelsByAiScorePercentile, calcAiScore } = require(path.join(root, "lib", "server", "leaderboard-scoring"));
const refPath = path.join(root, "reference", "rankmysalon-website.jsx");
const outPath = path.join(root, "sql", "014_seed_salon_ai_leaderboard.sql");

function slugify(name, id) {
  const base = String(name || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 48);
  return (base || "salon") + "-" + id;
}

function extractZipFromAddress(addr) {
  const matches = String(addr || "").match(/\b(\d{5})(?:-\d{4})?\b/g);
  if (!matches || !matches.length) return "";
  const last = matches[matches.length - 1];
  return last.length > 5 ? last.slice(0, 5) : last;
}

function sqlStr(s) {
  return "'" + String(s || "").replace(/'/g, "''") + "'";
}

const src = fs.readFileSync(refPath, "utf8");
const m = src.match(/const ALL_SALONS = (\[[\s\S]*?\])\.map\(/);
if (!m) {
  console.error("Could not parse ALL_SALONS from reference file.");
  process.exit(1);
}

// eslint-disable-next-line no-new-func
const raw = new Function("return " + m[1])();
if (!Array.isArray(raw) || !raw.length) {
  console.error("Parsed salons empty.");
  process.exit(1);
}

const rows = raw.map(function (s) {
  const score = calcAiScore(s.rating, s.reviews, s.p, s.f);
  const slug = slugify(s.name, s.id);
  return {
    slug,
    name: s.name,
    address: s.address,
    state: s.state,
    county: s.county,
    town: s.town,
    zipcode: extractZipFromAddress(s.address),
    category: s.category,
    rating: s.rating,
    review_count: s.reviews,
    phone: s.phone,
    sentiment_p: s.p,
    freshness_f: s.f,
    ai_score: score,
    assessment_level: "MODERATE",
  };
});
assignAssessmentLevelsByAiScorePercentile(rows);

const valueLines = rows.map(function (r) {
  return (
    "  (" +
    [
      sqlStr(r.slug),
      sqlStr(r.name),
      sqlStr(r.address),
      sqlStr(r.state),
      sqlStr(r.county),
      sqlStr(r.town),
      sqlStr(r.zipcode),
      sqlStr(r.category),
      String(r.rating),
      String(r.review_count),
      sqlStr(r.phone),
      String(r.sentiment_p),
      String(r.freshness_f),
      String(r.ai_score),
      sqlStr(r.assessment_level),
    ].join(", ") +
    ")"
  );
});

const header =
  "-- Generated by scripts/generate-leaderboard-seed-sql.js — do not hand-edit rows.\n" +
  "insert into public.salon_ai_leaderboard\n" +
  "  (slug, name, address, state, county, town, zipcode, category, rating, review_count, phone, sentiment_p, freshness_f, ai_score, assessment_level)\n" +
  "values\n";

const sql =
  header +
  valueLines.join(",\n") +
  "\non conflict (slug) do update set\n" +
  "  name = excluded.name,\n" +
  "  address = excluded.address,\n" +
  "  state = excluded.state,\n" +
  "  county = excluded.county,\n" +
  "  town = excluded.town,\n" +
  "  zipcode = excluded.zipcode,\n" +
  "  category = excluded.category,\n" +
  "  rating = excluded.rating,\n" +
  "  review_count = excluded.review_count,\n" +
  "  phone = excluded.phone,\n" +
  "  sentiment_p = excluded.sentiment_p,\n" +
  "  freshness_f = excluded.freshness_f,\n" +
  "  ai_score = excluded.ai_score,\n" +
  "  assessment_level = excluded.assessment_level,\n" +
  "  updated_at = now();\n";

fs.writeFileSync(outPath, sql, "utf8");
console.log("Wrote", outPath, "(" + rows.length + " rows)");
